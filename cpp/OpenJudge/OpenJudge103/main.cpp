#include <iostream>
using namespace std;
int main()
{
	int A;
	int B;
	cin >> A >> B;;
	cout << A << " + " << B << " = " << A + B << endl;
	cout << A << " - " << B << " = " << A - B <<endl;
	cout << A << " * " << B << " = " << A * B << endl;
	cout << A << " / " << B << " = " << A / B <<endl;
	return 0;
}
